int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 65 && str[i] <= 90) || (str[i] >= 'a' && str[i] <= 'z'))
		{
			str++;
			continue ;
		}
		return (0);
	}
	return (1);
}
