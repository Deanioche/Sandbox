-- -----------------------------------------------------
-- Table `학생`
-- -----------------------------------------------------
DROP TABLE IF EXISTS `학생` ;

CREATE TABLE IF NOT EXISTS `학생` (
    `학생번호` CHAR(13) NOT NULL,
    `학생이름` CHAR(30) NOT NULL,
    `성별` CHAR(4) NOT NULL CHECK (성별 IN ('남', '여')),
    `생년월일` DATE NULL,
    `나이` INT NULL,
    `전화번호` CHAR(15) NULL,
    PRIMARY KEY (`학생번호`),
    UNIQUE INDEX `학생번호_UNIQUE` (`학생번호` ASC))
    ENGINE = InnoDB;

-- -----------------------------------------------------
-- Table `계좌`
-- -----------------------------------------------------
DROP TABLE IF EXISTS `계좌` ;

CREATE TABLE IF NOT EXISTS `계좌` (
    `계좌번호` CHAR(15) NOT NULL,
    `잔액` INT NOT NULL,
    `학생번호` CHAR(13) NOT NULL,
    PRIMARY KEY (`계좌번호`, `학생번호`),
    INDEX `fk_계좌_학생1_idx` (`학생번호` ASC),
    UNIQUE INDEX `계좌번호_UNIQUE` (`계좌번호` ASC),
    CONSTRAINT `fk_계좌_학생1`
    FOREIGN KEY (`학생번호`)
    REFERENCES `학생` (`학생번호`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION)
    ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `학과`
-- -----------------------------------------------------
DROP TABLE IF EXISTS `학과` ;

CREATE TABLE IF NOT EXISTS `학과` (
    `학과이름` CHAR(50) NOT NULL,
    `단과대학` CHAR(30) NOT NULL,
    `주소`     VARCHAR(200) NULL,
    `전화번호` CHAR(15) NULL,
    `졸업학점` INT NOT NULL,
    PRIMARY KEY (`학과이름`))
    ENGINE = InnoDB;

-- -----------------------------------------------------
-- Table `교수`
-- -----------------------------------------------------
DROP TABLE IF EXISTS `교수` ;

CREATE TABLE IF NOT EXISTS `교수` (
    `교수번호` CHAR(13) NOT NULL,
    `교수이름` CHAR(30) NOT NULL,
    `직위` CHAR(10) NOT NULL CHECK (직위 IN ('정교수', '부교수', '조교수', '강사')),
    `소속학과` CHAR(50) NOT NULL,
    `연봉` INT ,
    PRIMARY KEY (`교수번호`),
    INDEX `fk_교수_학과1_idx` (`소속학과` ASC),
    CONSTRAINT `fk_교수_학과1`
    FOREIGN KEY (`소속학과`)
    REFERENCES `학과` (`학과이름`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION)
    ENGINE = InnoDB;



-- -----------------------------------------------------
-- Table `과목`
-- -----------------------------------------------------
DROP TABLE IF EXISTS `과목` ;

CREATE TABLE IF NOT EXISTS `과목` (
    `과목코드` CHAR(5) NOT NULL,
    `과목명`   VARCHAR(200) NOT NULL,
    `학점`     INT NOT NULL,
    `선수과목` CHAR(5) NULL,
    `이수구분` CHAR(20) NULL CHECK (이수구분 IN ('전공필수', '일반선택', '교양')),
    `교수번호` CHAR(13) NOT NULL,
    PRIMARY KEY (`과목코드`),
    INDEX `fk_과목_과목1_idx` (`선수과목` ASC) VISIBLE,
    INDEX `fk_과목_교수1_idx` (`교수번호` ASC) VISIBLE,
    CONSTRAINT `fk_과목_과목1`
    FOREIGN KEY (`선수과목`)
    REFERENCES `과목` (`과목코드`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
    CONSTRAINT `fk_과목_교수1`
    FOREIGN KEY (`교수번호`)
    REFERENCES `교수` (`교수번호`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION)
    ENGINE = InnoDB;


